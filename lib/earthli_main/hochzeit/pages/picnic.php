<h3>Picnic</h3>
<p>On the day after the nice dinner at the Beardslee, there will be a picnic at the <span class="field">Mohawk Fish &amp; Game Club</span>.
	The picnic starts at noon on September 15th and continues until around 16:00.</p>
<h4 id="directions">Directions</h4>
The Club is on the left of Route 28 South, 2.7 miles past the FastTrac in Mohawk.
<dl>
	<dt class="field">From Utica and points West of Mohawk</dt>
	<dd>
		<ol>
			<li>Take Route 5S East until the traffic light for Mohawk.</li>
			<li>Turn right at the light onto Route 28 South.</li>
		</ol>
	</dd>
	<dt class="field">From Little Falls and points East of Mohawk</dt>
	<dd>
		<ol>
			<li>Take Route 5 West.</li>
			<li>Take a left at the last light in Herkimer, toward Route 28 South.</li>
			<li>Take a right at the next light (going under the bridge).</li>
			<li>Go under the Thruway, past the Thruway entrance and over the river. Merge right onto Route 5S.</li>
			<li>Turn left at the first light onto Route 28 South.</li>
		</ol>
	</dd>
</dl>
