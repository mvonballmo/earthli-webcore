<h3>Reception</h3>
<p>The reception will take place on September 14th, 2002 at the <a class="field" href="http://www.beardsleecastle.com/">Beardslee Castle</a>, just outside of Little Falls,
	New York. There will be a small ceremony at 17:30, followed by dinner.</p>
<h4 id="directions">Directions</h4>
<p>The castle itself is almost exactly 3&frac12; hours from New York City.</p>
<dl>
<dt class="field">From Utica and points West of Beardslee Castle</dt>
<dd>
	<ol>
		<li>Take the NYS Thruway to Exit 29A (Little Falls).</li>
		<li>After the toll booth, drive straight for 2.5 miles.</li>
		<li>Turn right onto Route 5 (away from Little Falls).</li>
		<li>Beardslee Castle is on the left side (6 miles, about 1 mile after going from 4 to 2 lanes).</li>
	</ol>
</dd>
<dt class="field">From Albany and points East of Beardslee Castle</dt>
<dd>
	<ol>
		<li>Take the NYS Thruway to Exit 29 (Canajoharie).</li>
		<li>After the toll booth, turn right at the first stop sign.</li>
		<li>Turn right at the first light onto Route 10 North (200 yards).</li>
		<li>Cross the bridge and turn left at the next light onto Route 5 West (1/4 mile).</li>
		<li>Beardslee Castle is on the right side (13 miles).</li>
	</ol>
</dd>
</dl>
